INSERT INTO branch_stock (
    product_id,
    branch_id,
    quantity,
    reorder_level,
    last_updated
)
SELECT
    p.id,
    b.id,
    SUM(ps.quantity::numeric) AS quantity,
    0,
    NOW()
FROM purchase_stock_data ps
JOIN products p ON trim(p.name) = trim(ps.name)
JOIN branches b ON b.name = ps.branch_name
GROUP BY p.id, b.id
ON CONFLICT (product_id, branch_id) DO NOTHING;
